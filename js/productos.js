function mostrarProductos() {
  const contenedor = document.getElementById('productos-container');
  contenedor.classList.add('productos-grid');

  arayproductos.forEach((producto) => {
    const card = document.createElement('div');
    card.className = 'producto-card';

    const botonPrecio = document.createElement('button');
    botonPrecio.classList.add('botonPrecio');
    botonPrecio.textContent = 'Consultar precio';
    botonPrecio.addEventListener('click', () => {
      const mensaje = `Hola Jaguar Overo, me gustaría saber el precio de ${producto.nombre}`;
      const url = `https://api.whatsapp.com/send?phone=5491157493973&text=${encodeURIComponent(mensaje)}`;
      window.open(url, '_blank');
    });

    card.innerHTML = `
      <img src="${producto.img}" alt="${producto.alt}" class="producto-imagen" />
      <h3 class="producto-nombre">${producto.nombre}</h3>
      <p class="producto-descripcion">${producto.descripcion}</p>
      <div class="producto-footer">
        
      </div>
    `;
    //<span class="producto-precio">$${producto.moneda.toLocaleString()}</span>
    
    // Agregar el botón de "Consultar precio" al footer
    const footer = card.querySelector('.producto-footer');
    footer.appendChild(botonPrecio);

    contenedor.appendChild(card);
  });
}

document.addEventListener('DOMContentLoaded', mostrarProductos);  